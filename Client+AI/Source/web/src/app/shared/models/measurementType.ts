/**
 *Type of a measurement representing a sensor e.g. `area/section/position/[type]`
 *
 * @export
 * @class MeasurementType
 */
export class MeasurementType {
    /**
     *Name of the measurement type
     *
     * @memberof MeasurementType
     */
    name = '';

    /**
     *position of the sensor
     *
     * @memberof MeasurementType
     */
    position ? = '';
}
